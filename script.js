const calcModal  = document.getElementById('calcModal');
let selectedCur  = 'USD';

function openCalcModal() {
    calcModal.classList.add('open');
    document.body.style.overflow = 'hidden';
    updateAllRateCards();
    updateModalCalcResult();
}

function closeCalcModal() {
    calcModal.classList.remove('open');
    document.body.style.overflow = '';
}

calcModal.addEventListener('click', e => { if (e.target === calcModal) closeCalcModal(); });

function selectCurrency(cur) {
    selectedCur = cur;
    document.querySelectorAll('.calc-currency-card').forEach(c => {
        c.classList.toggle('active-cur', c.id === 'cur-' + cur);
    });
    updateModalCalcResult();
}

function updateAllRateCards() {
    Object.keys(baseRates).forEach(cur => {
        const el = document.getElementById('rate-' + cur);
        if (!el) return;
        const sdg = getSdgRate(cur);
        const t   = i18n[currentLang];
        el.textContent = Math.round(sdg).toLocaleString('ar') + ' ' + (t.calcLiveSdg || 'ج.س');
    });
}

function updateModalCalcResult() {
    const amtEl = document.getElementById('modalAmt');
    if (!amtEl) return;
    const amt    = parseFloat(amtEl.value) || 0;
    const rate   = getSdgRate(selectedCur);
    const result = amt * rate;
    const t      = i18n[currentLang];
    const resEl  = document.getElementById('modalResult');
    const eqEl   = document.getElementById('modalEq');
    if (resEl) resEl.textContent = Math.round(result).toLocaleString('ar') + ' ' + (t.calcLiveSdg || 'ج.س');
    if (eqEl)  eqEl.textContent  = amt + ' ' + selectedCur + ' = ' + Math.round(result).toLocaleString('ar') + ' ' + (t.calcLiveSdg || 'ج.س');
}

const modalAmtInput = document.getElementById('modalAmt');
if (modalAmtInput) modalAmtInput.addEventListener('input', updateModalCalcResult);

